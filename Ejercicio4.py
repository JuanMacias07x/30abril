y = int(input("Ingrese valor de lado 2:  "))
suma = x+y
resta = x-y
multiplicacion = x*y
division = x/y
residuo = x%y

print("La resultado es: ", "suma:", suma, "resta:", resta, "multiplicación:", multiplicacion, "división:", division, "residuo:", residuo)





